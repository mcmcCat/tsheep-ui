<script setup>
import { ref, computed } from 'vue';
import '@/assets/icon/iconfont.css';


// isShow控制显示隐藏
const isShow = ref(false);

// 定时器标识
let timer = null;

// margin-top响应式变量
const marginTop = ref(20);
// z-index响应式变量
const zIndex = ref(999);

const props = defineProps({
  type: {
    type: String,
    default: 'success',
  },
  message: {
    type: String,
    default: 'success~',
  },
})

// 类名变量，默认有message类，还有个动态的样式根据type来添加类名
const styleClass = ['message', props.type];

const iconClass = computed(() => {
  if (props.type === 'success') {
    return ['iconfont', 'icon-ok-block'];
  } else if (props.type === 'warning') {
    return ['iconfont', 'icon-exclamation-point-block'];
  } else {
    return ['iconfont', 'icon-close-block'];
  }
});

// 设置是否显示message元素（有v-show这样的切换显示才会触发动画）
function setIsShow(flag) {
  // 返回promise是因为需要把同步变异步，不能show完就hide
  return new Promise(resolve => {
    isShow.value = flag;
    timer = setTimeout(() => {
      clearTimeout(timer);
      timer = null;
      resolve('fade动画执行完成');
    }, 300);
  });
}

// 设置margin-top的方法
function setTop(newTop, newZIndex) {
  marginTop.value = newTop;
  zIndex.value = newZIndex;
}

// 暴露方法和属性到实例上（让index.ts中可以使用）
defineExpose({
  setIsShow,
  setTop,
  height: 30, // 信息框的高度
  consTop: 20, // 信息框的固定间距
  consZIndex: 999, // 最低层级
});
</script>

<template>
  <!-- 设置动画名字，在style中设置对应类选择器，并设置动画 -->
  <transition name="message-fade">
    <!-- isShow控制显示隐藏（有了它才可触发动画） -->
    <!-- marginTop动态的计算，让message不间断地堆叠 -->
    <!-- styleClass给不同类型的message设置类名 -->
    <!-- {{message}} 展示传递过来的message-->
    <div v-show="isShow" :style="{ 'margin-top': marginTop + 'px', 'z-index': zIndex }" :class="styleClass">
      <i :class="iconClass"></i>
      {{ message }}
    </div>
  </transition>
</template>

